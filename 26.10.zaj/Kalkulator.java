import java.util.*;
public class Kalkulator
{

    public int dodawanie(int a,int b)
    {
        return a+b;
    }
    public int odejmowanie(int a,int b)
    {
        return a-b;
    }
    public int mnozenie(int a,int b)
    {
        return a*b;
    }
    public double dzielenie(double a,double b)
    {
        return a/b;
    }
}
